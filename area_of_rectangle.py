
l = int(input("Enter the length of rectangle: "))
b = int(input("Enter the breadth of rectangle: "))

a = l * b
p = 2 * (l + b)

print("The area of the rectangle whose length is", l, "and breadth is", b, "is", a)
print("The perimeter of the rectangle whose length is", l, "and breadth is", b, "is", p)
